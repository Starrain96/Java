package 복습;

public class 기본형심화 {

	public static void main(String[] args) {
		// 기본형 4가지
		// 정수, 실수, 문자, 논리
		// int, double, char, boolean
		double temp = 1.2;
		int floor = 10;
		char day = '화';
		boolean juice = false;
		
		String location = "수서역";
		
		System.out.println("오늘 온도는 " + temp + "도");
		System.out.println("내가 있는 층수는  " + floor + "층");
		System.out.println("오늘은   " + day + "요일");
		System.out.println("마실 것이 있습니까?   " + juice);
		System.out.println("내 근처의 역은  " + location + "입니다.");
	}

}
