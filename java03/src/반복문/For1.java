package 반복문;

public class For1 { 
	public static void main(String[] args) {
		for (int i = 1; i < 4; i++) {
			System.out.println(i + "내가 반복");
		}
		
		for (int i = 0; i < 100; i++) {
			System.out.println("내가 반복" + i);
		}
		
		for (int i = 0; i < 10; i++) {
			System.out.println((i+1) + "번째 실행 중");
		}
	}

}
